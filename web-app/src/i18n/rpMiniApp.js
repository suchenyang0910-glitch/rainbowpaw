export function rpMiniAppT(lang, key, vars) {
  const k = String(key || '')
  const langId = String(lang || 'EN').toUpperCase()
  const dict = DICT[langId] || DICT.EN
  let text = dict[k] || DICT.EN[k] || k

  if (vars && typeof vars === 'object') {
    for (const [vk, vv] of Object.entries(vars)) {
      text = text.replaceAll(`{${vk}}`, String(vv))
    }
  }

  return text
}
